export function getT(lang: Language) {
  return (key: string, vars?: Record<string, string | number>): string => {
    const str = translations[lang][key] ?? translations.id[key] ?? key;
    if (!vars) return str;
    return str.replace(/\{\{(\w+)\}\}/g, (_, k) => String(vars[k] ?? ""));
  };
}
